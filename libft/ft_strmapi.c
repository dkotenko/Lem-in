#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	strlength;
	unsigned int	i;

	if (!s || !f)
		return (NULL);
	strlength = ft_strlen((char *)s);
	str = (char *)malloc(sizeof(char) * strlength + 1);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
